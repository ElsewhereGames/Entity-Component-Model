package com.elsewhere_games.lib.entity.event;

/**
 * <p>Describes the various types of component changes an entity can undergo.</p>
 */
public enum ComponentChangeType {

	/**
	 * <p>Indicates a component has been added to an entity.</p>
	 */
	COMPONENT_ADDED,

	/**
	 * <p>Indicates a component has been removed from an entity.</p>
	 */
	COMPONENT_REMOVED

}
